package com.ims;


public class Item {
    
    private String itemName;
    private String itemId;
    private int itemCount;
    private int itemNo;
    
    
    public Item(String itemName, String itemId, int itemCount, int itemNo) {
        this.itemName = itemName;
        this.itemId = itemId;
        this.itemCount = itemCount;
        this.itemNo = itemNo;
    }
    
}